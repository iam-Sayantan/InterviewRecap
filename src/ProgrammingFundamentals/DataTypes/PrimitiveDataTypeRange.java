package ProgrammingFundamentals.DataTypes;

public class PrimitiveDataTypeRange
{
    public static void main(String[] args) {
        System.out.println("_________________________________");
        System.out.println("Byte min value : "+Byte.MIN_VALUE);
        System.out.println("Byte max value : "+Byte.MAX_VALUE);
        System.out.println("_________________________________");
        System.out.println("Integer min value : "+Integer.MIN_VALUE);
        System.out.println("Integer max values : "+Integer.MAX_VALUE);
        System.out.println("_________________________________");
        System.out.println("Short min value : "+Short.MIN_VALUE);
        System.out.println("Short max values : "+Short.MAX_VALUE);
        System.out.println("_________________________________");
        System.out.println("Float min value : "+Float.MIN_VALUE);
        System.out.println("Float max values : "+Float.MAX_VALUE);
        System.out.println("_________________________________");
        System.out.println("Double min value : "+Double.MIN_VALUE);
        System.out.println("Double max values : "+Double.MAX_VALUE);
        System.out.println("_________________________________");
    }
}
